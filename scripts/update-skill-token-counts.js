#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const SKILLS_DIR = path.join(ROOT, 'skills');
const README_PATH = path.join(ROOT, 'README.md');
const START_MARKER = '<!-- skill-token-counts:start -->';
const END_MARKER = '<!-- skill-token-counts:end -->';
const MARKDOWN_EXTENSIONS = new Set(['.md', '.markdown', '.mdx']);
const IGNORED_DIRS = new Set(['.git', 'node_modules', 'dist', 'build', '.next', '.turbo']);

function usage() {
  console.log(`Usage:
  node ./scripts/update-skill-token-counts.js
  node ./scripts/update-skill-token-counts.js --print

Scans each skill directory under ./skills and updates the generated README table with:
- top-level SKILL.md token estimate
- full skill token estimate across all Markdown prompt files in that skill

Estimate method:
- normalized character count / 4
- intentionally rough, but a common GPT-style approximation for English/Markdown
- scripts are excluded from the full-skill count because they are not loaded as prompt text
`);
}

function formatNumber(value) {
  return new Intl.NumberFormat('en-US').format(value);
}

function isMarkdownFile(filePath) {
  return MARKDOWN_EXTENSIONS.has(path.extname(filePath).toLowerCase());
}

function listMarkdownFiles(dirPath) {
  const files = [];
  for (const entry of fs.readdirSync(dirPath, { withFileTypes: true })) {
    if (entry.isDirectory()) {
      if (IGNORED_DIRS.has(entry.name)) continue;
      files.push(...listMarkdownFiles(path.join(dirPath, entry.name)));
      continue;
    }
    if (!entry.isFile()) continue;
    const fullPath = path.join(dirPath, entry.name);
    if (isMarkdownFile(fullPath)) files.push(fullPath);
  }
  return files.sort();
}

function normalizeForCounting(text) {
  return text.replace(/\r\n/g, '\n');
}

function estimateTokensFromChars(charCount) {
  return Math.ceil(charCount / 4);
}

function countChars(filePath) {
  return normalizeForCounting(fs.readFileSync(filePath, 'utf8')).length;
}

function findSkillDirs() {
  return fs
    .readdirSync(SKILLS_DIR, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => path.join(SKILLS_DIR, entry.name))
    .filter((dirPath) => fs.existsSync(path.join(dirPath, 'SKILL.md')))
    .sort();
}

function collectSkillStats(skillDir) {
  const markdownFiles = listMarkdownFiles(skillDir);
  const skillMdPath = path.join(skillDir, 'SKILL.md');
  const skillMdChars = countChars(skillMdPath);
  const fullSkillChars = markdownFiles.reduce((sum, filePath) => sum + countChars(filePath), 0);

  return {
    skill: path.basename(skillDir),
    markdownFileCount: markdownFiles.length,
    skillMdChars,
    fullSkillChars,
    skillMdTokens: estimateTokensFromChars(skillMdChars),
    fullSkillTokens: estimateTokensFromChars(fullSkillChars),
  };
}

function renderTable(stats) {
  const totalMarkdownFiles = stats.reduce((sum, item) => sum + item.markdownFileCount, 0);
  const totalSkillMdTokens = stats.reduce((sum, item) => sum + item.skillMdTokens, 0);
  const totalFullSkillTokens = stats.reduce((sum, item) => sum + item.fullSkillTokens, 0);

  const lines = [
    START_MARKER,
    '## Estimated skill token counts',
    '',
    '_Generated by `npm run update:tokens`. Rough estimate only: normalized characters / 4. “Full skill” counts all Markdown prompt files in the skill directory; scripts are excluded because they are not loaded as prompt text._',
    '',
    '| Skill | Top-level `SKILL.md` | Full skill markdowns | Markdown files counted |',
    '| --- | ---: | ---: | ---: |',
    ...stats.map((item) => `| \`${item.skill}\` | ${formatNumber(item.skillMdTokens)} | ${formatNumber(item.fullSkillTokens)} | ${formatNumber(item.markdownFileCount)} |`),
    `| **Total** | **${formatNumber(totalSkillMdTokens)}** | **${formatNumber(totalFullSkillTokens)}** | **${formatNumber(totalMarkdownFiles)}** |`,
    END_MARKER,
  ];

  return lines.join('\n');
}

function updateReadme(table) {
  const readme = fs.readFileSync(README_PATH, 'utf8');
  const blockPattern = new RegExp(`${START_MARKER}[\\s\\S]*?${END_MARKER}`);

  if (blockPattern.test(readme)) {
    return readme.replace(blockPattern, table);
  }

  const installHeading = '\n## Install\n';
  const index = readme.indexOf(installHeading);
  if (index === -1) {
    throw new Error('Could not find "## Install" in README.md to insert token-count table.');
  }

  return readme.slice(0, index).trimEnd() + '\n\n' + table + '\n' + readme.slice(index);
}

function main() {
  const args = process.argv.slice(2);
  if (args.includes('--help') || args.includes('-h')) {
    usage();
    process.exit(0);
  }

  const stats = findSkillDirs().map(collectSkillStats);
  const table = renderTable(stats);

  if (args.includes('--print')) {
    process.stdout.write(table + '\n');
    return;
  }

  const updatedReadme = updateReadme(table);
  fs.writeFileSync(README_PATH, updatedReadme, 'utf8');

  console.log('Updated README.md skill token count table:');
  for (const item of stats) {
    console.log(`- ${item.skill}: SKILL.md ${formatNumber(item.skillMdTokens)} tokens, full skill ${formatNumber(item.fullSkillTokens)} tokens across ${formatNumber(item.markdownFileCount)} markdown files`);
  }
}

main();
